package com.zegocloud.uikit.plugin.adapter.plugins.beauty;

public class ZegoBeautyPluginRect {

    public int x;
    public int y;
    public int width;
    public int height;

    public ZegoBeautyPluginRect() {
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getX() {
        return this.x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getY() {
        return this.y;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getWidth() {
        return this.width;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getHeight() {
        return this.height;
    }
}
